# A class that is used to implement the race system
class Race:
    def __init__(self, name: str):
        self.name = name

dragonborn = Race("Dragonborn")
dwarf = Race("Dwarf")
elf = Race("Elf")
gnome = Race("Gnome")
half_elf = Race("Half-Elf")
halfin = Race("Halfing")
half_orc = Race("Half-Orc")
human = Race("Human")
tiefling = Race("Tiefling")